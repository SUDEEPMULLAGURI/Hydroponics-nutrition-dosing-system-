/******************************************************************************
 * @file    kernel.cpp
 ******************************************************************************/

#include "kernel.h"

#include "../hal/hal.h"

#include "scheduler/scheduler.h"

namespace kernel
{

namespace
{

bool initialized = false;

uint32_t tick = 0;

uint64_t uptime = 0;

float cpuUsage = 0.0f;

}

/******************************************************************************
 * Initialize
 ******************************************************************************/

Status Kernel::Init()
{
    if(initialized)
        return Status::Ok;

    if(HAL_Init() != HAL_OK)
        return Status::Error;

    if(Scheduler::Init() != Status::Ok)
        return Status::Error;

    initialized = true;

    return Status::Ok;
}

/******************************************************************************
 * Main Kernel Loop
 ******************************************************************************/

void Kernel::Run()
{
    if(!initialized)
        return;

    HAL_Update();

    Scheduler::Run();

    tick++;

    uptime++;
}

/******************************************************************************
 * Shutdown
 ******************************************************************************/

void Kernel::Shutdown()
{
    initialized = false;
}

/******************************************************************************
 * Reset
 ******************************************************************************/

void Kernel::Reset()
{
    Scheduler::Reset();

    tick = 0;

    uptime = 0;
}

/******************************************************************************
 * Tick
 ******************************************************************************/

uint32_t Kernel::Tick()
{
    return tick;
}

/******************************************************************************
 * Uptime
 ******************************************************************************/

uint64_t Kernel::Uptime()
{
    return uptime;
}

/******************************************************************************
 * CPU Usage
 ******************************************************************************/

float Kernel::CpuUsage()
{
    return cpuUsage;
}

/******************************************************************************
 * State
 ******************************************************************************/

bool Kernel::Initialized()
{
    return initialized;
}

}
